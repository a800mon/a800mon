def screen_to_atascii(b):
    c = b & 0x7F
    if c < 64:
        c += 32
    elif c < 96:
        c -= 64
    return c | (b & 0x80)


def atascii_to_curses(a):
    inv = a & 0x80
    ch = ATASCII[a & 0x7F]  # Twoja mapa ATASCII->UTF8
    attr = curses.A_REVERSE if inv else 0
    return ch, attr
