_HEX_ADDR_RE = re.compile(r"\$([0-9A-Fa-f]{1,4})")

def lookup_symbol(addr: int) -> str | None:
    return _SYMBOLS.get(addr & 0xFFFF)

def comment_for_asm(asm_text: str) -> str:
    parts = asm_text.split(None, 1)
    if len(parts) < 2:
        return ""
    operand = parts[1].strip()
    if not operand or operand.startswith("#"):
        return ""
    match = _HEX_ADDR_RE.search(operand)
    if not match:
        return ""
    addr = int(match.group(1), 16)
    symbol = lookup_symbol(addr)
    if not symbol:
        return ""
    return f";{symbol}"
